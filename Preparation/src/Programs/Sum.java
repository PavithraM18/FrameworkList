package Programs;

public class Sum {

	 public static void main(String[] args) {
	        String[] s = {"1", "2", "3"};

	        int sum = 0;
	        for (int i = 0; i < s.length; i++) {
	            sum = sum + Integer.parseInt(s[i]);
	        }

	        System.out.println(sum); // Output: 6
	    }

}